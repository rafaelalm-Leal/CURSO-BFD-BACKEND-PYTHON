"""Faça um Programa que leia 20 números inteiros e armazene-os numa lista. Armazene os números pares na lista PAR 
e os números IMPARES na lista impar. Imprima as três listas."""
nums=[]
pares=[]
impares=[]
for num in range(1, 21):
    nums.append(num)
    if num % 2 == 0:
        pares.append(num)
    else:
        impares.append(num)
print(f"lista dos numero {nums}")
print(f"lista dos pares {pares}")
print(f"lista dos impares {impares}")
