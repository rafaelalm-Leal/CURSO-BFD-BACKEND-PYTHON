"""Faça um Programa que leia uma lista A com 10 números inteiros, calcule e mostre a soma dos quadrados dos elementos do vetor."""
# Criando a lista
A = []
for i in range(10):
    num = int(input(f'Digite o {i+1}º número: '))
    A.append(num)

# Calculando a soma dos quadrados
soma_quadrados = 0
for num in A:
    soma_quadrados += num ** 2

# Resultado
print('A lista é:', A)
print('A soma dos quadrados dos elementos é:', soma_quadrados)
